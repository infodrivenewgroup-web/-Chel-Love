// Генерация списка автоматических статей (каждые 48 часов)
export function getAutoGeneratedArticles(count = 20): ReturnType<typeof generateArticle>[] {
  const articles: ReturnType<typeof generateArticle>[] = []
  const now = new Date()

  for (let i = 0; i < count; i++) {
    const articleDate = new Date(now)
    // Каждая статья публикуется через 48 часов после предыдущей
    articleDate.setDate(articleDate.getDate() - i * 2)
    articleDate.setHours(10, 0, 0, 0)

    if (articleDate <= now) {
      articles.push(generateArticle(i, articleDate))
    }
  }

  return articles
}

// Получить топ-3 статьи для главной страницы
export function getTopArticlesForHomepage(): ReturnType<typeof generateArticle>[] {
  const allArticles = getAutoGeneratedArticles(20)
  // Сортируем по просмотрам и берём топ-3
  return allArticles.sort((a, b) => b.views - a.views).slice(0, 3)
}

