// Функция генерации уникального seed на основе даты
function getDateSeed(date: Date): number {
  const year = date.getFullYear()
  const month = date.getMonth()
  const day = date.getDate()
  return year * 10000 + month * 100 + day
}

// Псевдослучайный генератор на основе seed
function seededRandom(seed: number): () => number {
  let s = seed
  return () => {
    s = (s * 9301 + 49297) % 233280
    return s / 233280
  }
}

// Функция выбора случайного элемента
function pickRandom<T>(arr: T[], random: () => number): T {
  return arr[Math.floor(random() * arr.length)]
}

// Функция перемешивания массива
function shuffle<T>(arr: T[], random: () => number): T[] {
  const result = [...arr]
  for (let i = result.length - 1; i > 0; i--) {
    const j = Math.floor(random() * (i + 1))
    ;[result[i], result[j]] = [result[j], result[i]]
  }
  return result
}

// Генерация уникальных просмотров на основе seed статьи
function generateUniqueViews(seed: number, articleIndex: number): number {
  const random = seededRandom(seed + articleIndex * 777)
  // Базовые просмотры от 1200 до 8000
  const baseViews = 1200 + Math.floor(random() * 6800)
  // Множитель "возраста" статьи (старые статьи имеют больше просмотров)
  const ageMultiplier = 1 + articleIndex * 0.15
  // Случайный бонус для разнообразия
  const randomBonus = Math.floor(random() * 2000)
  return Math.floor(baseViews * ageMultiplier + randomBonus)
}

// Генерация уникального времени чтения
